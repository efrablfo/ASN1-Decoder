package com.asn1.parser;

public class NullObject {
    private Class clazz;

    public NullObject(Class clazz) {
        this.clazz = clazz;
    }

    public Class getClazz() {
        return clazz;
    }

    public void setClazz(Class clazz) {
        this.clazz = clazz;
    }
}
